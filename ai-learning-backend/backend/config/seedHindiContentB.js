async function main() {
  await mongoose.connect(process.env.MONGO_URI, { serverSelectionTimeoutMS: 10_000 });
  console.log("MongoDB connected");

  let created = 0;
  let updated = 0;

  for (const rec of records) {
    const existing = await NcertTopicContent.findOne({ topicId: rec.topicId });
    if (existing) {
      await NcertTopicContent.updateOne({ topicId: rec.topicId }, { $set: rec });
      updated++;
    } else {
      await NcertTopicContent.create(rec);
      created++;
    }
    console.log(`  ✓ ${rec.topicId} — ${rec.name}`);
  }

  console.log(`\nDone: ${created} created, ${updated} updated`);
  await mongoose.disconnect();
}

main().catch((err) => { console.error(err); process.exit(1); });
